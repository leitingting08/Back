#include<stdio.h>

int main()
{
    //使用二维数组表示学生的成绩表
    //四个学生的3门课成绩 ：4行3列
    int i,j;
    double scores[4][3] = {
        {98,67,89},
        {95,97,84},
        {89,87,83},
        {78,77,91}
    };
    //使用循环打印每个数组元素
    printf("语文    数学    英语 \n");
    for(i=0;i<4;i++)
    {
        for(j=0;j<3;j++)
        {
            printf("%-8.2lf", scores[i][j] ); //数字占八个字符，-号表示左对齐
        }
        printf(" \n");
    }
}
